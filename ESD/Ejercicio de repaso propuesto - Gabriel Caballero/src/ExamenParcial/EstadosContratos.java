package ExamenParcial;

public class EstadosContratos {
    private Integer idEstadosContratos;
    private String descrip, comentario;

    public void EstadosContratos(){

    }

    public Integer getIdEstadosContratos() {
        return idEstadosContratos;
    }

    public void setIdEstadosContratos(Integer idEstadosContratos) {
        this.idEstadosContratos = idEstadosContratos;
    }

    public String getDescrip() {
        return descrip;
    }

    public void setDescrip(String descrip) {
        this.descrip = descrip;
    }

    public String getComentario() {
        return comentario;
    }

    public void setComentario(String comentario) {
        this.comentario = comentario;
    }
}
