package ExamenParcial;

public class TiposContratos {
    private Integer idTiposContratos;
    private String descrip, comentario;

    public void TiposContratos(){

    }

    public Integer getIdTiposContratos() {
        return idTiposContratos;
    }

    public void setIdTiposContratos(Integer idTiposContratos) {
        this.idTiposContratos = idTiposContratos;
    }

    public String getDescrip() {
        return descrip;
    }

    public void setDescrip(String descrip) {
        this.descrip = descrip;
    }

    public String getComentario() {
        return comentario;
    }

    public void setComentario(String comentario) {
        this.comentario = comentario;
    }
}
